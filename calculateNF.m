function NF =  calculateNF(G, F) %takes real numbers as values
NF = F(1) + (F(2) - 1) / G(1) + (F(3) - 1) / (G(1) * G(2))...
    +(F(4) - 1) / (G(1) * G(2) *G(3))  + (F(5) - 1) / (G(1) * G(2) *G(3)*G(4))...
    +(F(6) - 1) / (G(1) * G(2) *G(3)*G(4)*G(5)) + (F(7) - 1) / (G(1) * G(2) *G(3)*G(4)*G(5)*G(6));
end